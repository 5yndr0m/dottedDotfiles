command -v notify-send >/dev/null || missing_deps+=("libnotify")

# Optional but recommended
command -v matugen >/dev/null || echo "âš ï¸  matugen not found - install for color theming"
command -v swww >/dev/null || command -v swaybg >/dev/null || command -v hyprpaper >/dev/null || echo "âš ï¸  No wallpaper daemon found - install swww, swaybg, or hyprpaper"

